const (
	processEventPageMax                = 16
	maxProcessEventPayloadDisplayBytes = 512
)

var errProcessDaemonAlreadyReported = errors.New("daemon requirement already reported")

type processRunJSON struct {
	ID                    string            `json:"id"`
	TemplateRef           string            `json:"templateRef"`
	Params                map[string]string `json:"params"`
	ProgramAuthorizations []string          `json:"programAuthorizations"`
	Status                engine.RunStatus  `json:"status"`
	StateVersion          int64             `json:"stateVersion"`
	Checkpoint            engine.Checkpoint `json:"checkpoint"`
	Action                string            `json:"action"`
	NeedsReconcile        bool              `json:"needsReconcile"`
	CreatedAt             time.Time         `json:"createdAt"`
	UpdatedAt             time.Time         `json:"updatedAt"`
}

type processRunSummaryJSON struct {
	ID           string           `json:"id"`
	TemplateRef  string           `json:"templateRef"`
	Status       engine.RunStatus `json:"status"`
	StateVersion int64            `json:"stateVersion"`
	CreatedAt    time.Time        `json:"createdAt"`
	UpdatedAt    time.Time        `json:"updatedAt"`
}

type processRunEventJSON struct {
	Sequence   int64           `json:"sequence"`
	OccurredAt time.Time       `json:"occurredAt"`
	NodeID     string          `json:"nodeId"`
	Kind       string          `json:"kind"`
	Payload    json.RawMessage `json:"payload"`
	Actor      string          `json:"actor"`
}

type processRunParams struct {
	TemplateID string   `pos:"true" help:"Saved process-template id to instantiate"`
	RunID      string   `long:"run-id" optional:"true" help:"Explicit run id (normally generated by agentd)"`
	Params     []string `long:"param" optional:"true"`
	Authorize  []string `long:"authorize-program-profile" optional:"true"`
	AskHuman   string   `long:"ask-human" optional:"true" help:"On permission denial, ask the human via popup with this timeout"`
}

func processRunCmd() *cobra.Command {
	return boa.CmdT[processRunParams]{
		Use:         "run",
		Short:       "Instantiate and run the saved template head through agentd",
		Long:        "Pins the exact saved template head and initial checkpoint transactionally. Repeat --param key=value and --authorize-program-profile profile for every exact program profile the run may execute. Use --authorize-program-profile= to explicitly authorize an empty profile.",
		ParamEnrich: common.DefaultParamEnricher(),
		InitFuncCtx: func(ctx *boa.HookContext, p *processRunParams, cmd *cobra.Command) error {
			boa.GetParamT(ctx, &p.Params).SetNoFlag(true)
			cmd.Flags().StringArrayVar(&p.Params, "param", nil, "Template parameter key=value. Repeatable; values may contain commas or '='.")
			boa.GetParamT(ctx, &p.Authorize).SetNoFlag(true)
			cmd.Flags().StringArrayVar(&p.Authorize, "authorize-program-profile", nil, "Explicitly authorize one exact program profile for this run. Repeatable.")
			return nil
		},
		RunFunc: func(p *processRunParams, _ *cobra.Command, _ []string) {
			exitProcessRuntime(runProcessRun(p, os.Stdout, os.Stderr), os.Stderr)
		},
	}.ToCobra()
}

func runProcessRun(p *processRunParams, stdout, stderr io.Writer) error {
	if err := requireProcessRuntime(stderr); err != nil {
		return err
	}
	params, err := parseProcessParams(p.Params)
	if err != nil {
		return err
	}
	ask, err := agent.ParseAskHuman(p.AskHuman)
	if err != nil {
		return err
	}
	request := struct {
		ID                       string            `json:"id,omitempty"`
		TemplateID               string            `json:"templateId"`
		Params                   map[string]string `json:"params"`
		AuthorizeProgramProfiles []string          `json:"authorizeProgramProfiles"`
	}{
		ID: strings.TrimSpace(p.RunID), TemplateID: strings.TrimSpace(p.TemplateID),
		Params: params, AuthorizeProgramProfiles: append([]string(nil), p.Authorize...),
	}
	if request.TemplateID == "" {
		return fmt.Errorf("template id is required")
	}
	var run processRunJSON
	if err := agent.DaemonRequest(http.MethodPost, "/v1/process/runs", request, &run,
		agent.DaemonOpts{AskHuman: ask, RetryOutput: stderr}); err != nil {
		return err
	}
	fmt.Fprintf(stdout, "Created process run %s\n", run.ID)
	printProcessRun(stdout, &run)
	fmt.Fprintf(stdout, "Next: tclaude process show %s; tclaude process events %s\n", run.ID, run.ID)
	return nil
}

type processRunsLsParams struct {
	After string `long:"after" optional:"true" help:"Exclusive run-id cursor"`
	Limit int    `long:"limit" optional:"true" help:"Maximum rows (1..32)"`
}

func processRunsLsCmd() *cobra.Command {
	return boa.CmdT[processRunsLsParams]{
		Use:         "ls",
		Short:       "List daemon-owned process runs",
		ParamEnrich: common.DefaultParamEnricher(),
		RunFunc: func(p *processRunsLsParams, _ *cobra.Command, _ []string) {
			exitProcessRuntime(runProcessRunsLs(p, os.Stdout, os.Stderr), os.Stderr)
		},
	}.ToCobra()
}

func runProcessRunsLs(p *processRunsLsParams, stdout, stderr io.Writer) error {
	if err := requireProcessRuntime(stderr); err != nil {
		return err
	}
	query := url.Values{}
	if after := strings.TrimSpace(p.After); after != "" {
		query.Set("after", after)
	}
	if p.Limit != 0 {
		if p.Limit < 1 || p.Limit > 32 {
			return fmt.Errorf("--limit must be 1..32")
		}
		query.Set("limit", strconv.Itoa(p.Limit))
	}
	path := "/v1/process/runs"
	if encoded := query.Encode(); encoded != "" {
		path += "?" + encoded
	}
	var response struct {
		Runs []processRunSummaryJSON `json:"runs"`
		Next string                  `json:"next"`
	}
	if err := agent.DaemonRequest(http.MethodGet, path, nil, &response, agent.DaemonOpts{RetryOutput: stderr}); err != nil {
		return err
	}
	if len(response.Runs) == 0 {
		fmt.Fprintln(stdout, "No process runs.")
		return nil
	}
	tw := newTable(stdout)
	fmt.Fprintln(tw, "ID\tTEMPLATE\tSTATUS\tUPDATED")
	for _, run := range response.Runs {
		fmt.Fprintf(tw, "%s\t%s\t%s\t%s\n", run.ID, run.TemplateRef, run.Status, formatTime(run.UpdatedAt))
	}
	if err := tw.Flush(); err != nil {
		return err
	}
	if response.Next != "" {
		fmt.Fprintf(stdout, "next=%s\n", response.Next)
	}
	return nil
}

