func runProcessRecordOutcome(p *processRecordOutcomeParams, stdout, stderr io.Writer) error {
	if err := requireProcessRuntime(stderr); err != nil {
		return err
	}
	outcome := engine.ProgramOutcome(strings.TrimSpace(p.Outcome))
	if outcome != engine.ProgramSucceeded && outcome != engine.ProgramFailed {
		return fmt.Errorf("--outcome must be succeeded or failed")
	}
	ask, err := agent.ParseAskHuman(p.AskHuman)
	if err != nil {
		return err
	}
	request := struct {
		Outcome  engine.ProgramOutcome `json:"outcome"`
		ExitCode int                   `json:"exitCode"`
		Error    string                `json:"error,omitempty"`
		Note     string                `json:"note,omitempty"`
	}{Outcome: outcome, ExitCode: p.ExitCode, Error: p.Error, Note: p.Note}
	var response struct {
		Started bool           `json:"started"`
		Run     processRunJSON `json:"run"`
	}
	path := "/v1/process/runs/" + url.PathEscape(strings.TrimSpace(p.RunID)) + "/record-outcome"
	if err := agent.DaemonRequest(http.MethodPost, path, request, &response,
		agent.DaemonOpts{AskHuman: ask, RetryOutput: stderr}); err != nil {
		return err
	}
	fmt.Fprintf(stdout, "Recorded outcome for process run %s.\n", response.Run.ID)
	printProcessRun(stdout, &response.Run)
	return nil
}

func fetchProcessRun(runID string, stderr io.Writer) (*processRunJSON, error) {
	runID = strings.TrimSpace(runID)
	if runID == "" {
		return nil, fmt.Errorf("run id is required")
	}
	var run processRunJSON
	if err := agent.DaemonRequest(http.MethodGet, "/v1/process/runs/"+url.PathEscape(runID), nil, &run,
		agent.DaemonOpts{RetryOutput: stderr}); err != nil {
		return nil, err
	}
	return &run, nil
}

func parseProcessParams(values []string) (map[string]string, error) {
	params := make(map[string]string, len(values))
	for _, value := range values {
		key, param, ok := strings.Cut(value, "=")
		key = strings.TrimSpace(key)
		if !ok || key == "" {
			return nil, fmt.Errorf("--param %q must be key=value", value)
		}
		if _, exists := params[key]; exists {
			return nil, fmt.Errorf("--param repeats key %q", key)
		}
		params[key] = param
	}
	return params, nil
}

func requireProcessRuntime(stderr io.Writer) error {
	if err := requireProcessesEnabled(); err != nil {
		return err
	}
	if agent.RequireDaemonOrExit(stderr) != 0 {
		return errProcessDaemonAlreadyReported
	}
	return nil
}

func printProcessRun(out io.Writer, run *processRunJSON) {
	fmt.Fprintf(out, "id=%s\ntemplate=%s\nstatus=%s\naction=%s\nstateVersion=%d\n",
		run.ID, run.TemplateRef, run.Status, run.Action, run.StateVersion)
	if run.NeedsReconcile && run.Checkpoint.OutstandingCommand != nil {
		command := run.Checkpoint.OutstandingCommand
		fmt.Fprintf(out, "needsReconcile=true\ncommand=%s\nnode=%s\nprofile=%s\nprogram=%s\n",
			command.ID, command.NodeID, command.Program.Profile, command.Program.Run)
	}
}

func exitProcessRuntime(err error, stderr io.Writer) {
	if err == nil {
		return
	}
	if !errors.Is(err, errProcessDaemonAlreadyReported) {
		fmt.Fprintf(stderr, "Error: %v\n", err)
	}
	os.Exit(1)
}
